#!/usr/bin/env node
/**
 * Generates ui/utils/icon-data.ts — an inline map of the hot-path SVG icons so
 * the IconLoader can seed its cache at construction and skip per-icon network
 * fetches for the icons the demo/editor actually render on load.
 *
 * Long-tail icons (social/brand marks, rarely-used glyphs) are intentionally
 * NOT inlined; they keep loading lazily via fetch to avoid bloating the bundle.
 *
 * Regenerate after adding/removing a hot icon:  node scripts/gen-icon-data.js
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const ICON_DIR = path.join(__dirname, "..", "ui", "assets", "icons");
const OUT_FILE = path.join(__dirname, "..", "ui", "utils", "icon-data.ts");

// Hot path: editor toolbar + UI chrome + theme toggle. Keep this list in sync
// with the icons rendered on first paint. Long-tail brand icons stay on fetch.
const HOT_ICONS = [
  "undo",
  "redo",
  "bold",
  "italic",
  "strikethrough",
  "code",
  "list-ul",
  "list-ol",
  "checkbox",
  "quote",
  "code-block",
  "divider",
  "link",
  "image",
  "table",
  "math",
  "export",
  "fullscreen",
  "plus",
  "close",
  "menu",
  "sun",
  "moon",
  "flame",
  "snowflake",
];

/** Collapse insignificant whitespace; formatSvg re-parses so this is lossless. */
function minify(svg) {
  return svg.replace(/>\s+</g, "><").replace(/\s+/g, " ").trim();
}

const entries = [];
for (const name of HOT_ICONS) {
  const file = path.join(ICON_DIR, `${name}.svg`);
  if (!fs.existsSync(file)) {
    console.error(`[gen-icon-data] missing icon: ${name}.svg`);
    process.exit(1);
  }
  const svg = minify(fs.readFileSync(file, "utf8"));
  entries.push(`  ${JSON.stringify(name)}: ${JSON.stringify(svg)},`);
}

const banner = `/**
 * AUTO-GENERATED by scripts/gen-icon-data.js — do not edit by hand.
 *
 * Inline SVG for the hot-path icons rendered on first paint. The IconLoader
 * seeds its cache from this map so these icons need zero network fetches.
 * Long-tail icons are not here and continue to load lazily via fetch.
 */
`;

const body = `${banner}export const INLINE_ICONS: Readonly<Record<string, string>> = Object.freeze({
${entries.join("\n")}
});
`;

fs.writeFileSync(OUT_FILE, body);
console.log(
  `[gen-icon-data] wrote ${entries.length} inline icons -> ${path.relative(path.join(__dirname, ".."), OUT_FILE)}`,
);
